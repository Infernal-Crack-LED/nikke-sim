// Kit-role archetype tags (what a unit's kit DOES — buffer, reload buffer,
// distributed damage, …). Generated by scripts/build-archetype-tags.ts; see
// docs/data/archetype-tags.md. The `vocabulary` block carries the player-facing
// label + blurb so the UI never hardcodes them.
type ArchetypeTagsFile = {
  vocabulary: Record<string, { label: string; blurb: string; group: string }>;
  tags: Record<string, string[]>;
};
const archetypeData = archetypeTagsJson as unknown as ArchetypeTagsFile;

// Pills are bucketed into groups (Stat Buffs / Damage Buffs / Ally Filters /
// Burst Support / Role) for the filter panel. Group order = first appearance in
// the vocabulary and pill order within a group = vocabulary insertion order; both
// mirror the TAG_DEFS order in the build script, so the panel reads top-to-bottom
// in the same order the rules are defined there.
type ArchetypeOption = { id: string; label: string; blurb: string };
const ARCHETYPE_GROUPS: { group: string; options: ArchetypeOption[] }[] =
  (() => {
    const byGroup = new Map<string, ArchetypeOption[]>();
    for (const [id, v] of Object.entries(archetypeData.vocabulary)) {
      if (!byGroup.has(v.group)) byGroup.set(v.group, []);
      byGroup.get(v.group)!.push({ id, label: v.label, blurb: v.blurb });
    }
    return [...byGroup.entries()].map(([group, options]) => ({
      group,
      options,
    }));
  })();

export interface TeamBuilderProps {
  user: AuthUser | null;
  onSaveTeam: (slugs: (string | null)[]) => Promise<void>;
  // Returns a warning message (and does NOT switch tabs) when the team includes a
  // sim-unsupported nikke; returns null and switches to the Sim tab on success.
  onCopyToSim: (slugs: (string | null)[]) => string | null;
  onTeamChange: (slugs: (string | null)[]) => void;
}

// Icon-only filter row: a set of square icon buttons. Each button toggles on/off
// (multi-select). When no buttons in a row are selected, the filter is inactive
// (all values pass). When one or more are selected, only characters matching at
// least one selected value pass.
type FilterRowProps = {
  label: string;
  options: {
    id: string;
    icon: string;
    match?: (value: string) => boolean;
    imgStyle?: React.CSSProperties;
  }[];
  selected: Set<string>;
  onToggle: (id: string) => void;
  thumbs: Record<string, string>;
};

function FilterRow({
  label,
  options,
  selected,
  onToggle,
  thumbs,
}: FilterRowProps) {
  return (
    <div className='teambuilder-filter-row'>
      <span className='teambuilder-filter-label'>{label}</span>
      <div className='teambuilder-filter-icons'>
        {options.map((opt) => {
          const active = selected.has(opt.id);
          const src = `/nikke-icons/${opt.icon}`;
          // Tag icons that should keep their original colors when selected
          // (skip the brightness(0) filter). Weapon and burst icons are
          // monochrome but need to stay visible on the accent background.
          const isColored =
            opt.icon.startsWith('code_') ||
            opt.icon.startsWith('class_') ||
            opt.icon.startsWith('man_') ||
            opt.icon.startsWith('weapon_') ||
            opt.icon.startsWith('burst_');
          return (
            <button
              key={opt.id}
              className={'teambuilder-icon-btn' + (active ? ' on' : '')}
              onClick={() => onToggle(opt.id)}
              title={opt.id}
              aria-pressed={active}
            >
              <img
                src={src.endsWith('.svg') ? src : (thumbs[src] ?? src)}
                alt={opt.id}
                draggable={false}
                data-colored={isColored ? '' : undefined}
                style={opt.imgStyle}
              />
            </button>
          );
        })}
      </div>
    </div>
  );
}

